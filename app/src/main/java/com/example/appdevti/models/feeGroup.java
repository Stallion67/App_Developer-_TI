package com.example.appdevti.models;

public class feeGroup {

    private String name;
    private String description;
    private String itemId;
    private String payType;
    private String itemFeeId;
    private String item;
    private String clientFees;
    private String id;
    private String isActive;
    private String issueDate;


    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getItemId() {
        return itemId;
    }

    public String getPayType() {
        return payType;
    }

    public String getItemFeeId() {
        return itemFeeId;
    }

    public String getItem() {
        return item;
    }

    public String getClientFees() {
        return clientFees;
    }

    public String getId() {
        return id;
    }

    public String getIsActive() {
        return isActive;
    }

    public String getIssueDate() {
        return issueDate;
    }
}
